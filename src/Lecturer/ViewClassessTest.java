    // METHOD TO WRITE TABLE DATA TO classtest.txt
    private void updateClassTestFile() {
        try {
            // Create FileWriter - replace with your file path
            FileWriter writer = new FileWriter("TextFiles/classtest.txt");
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

            // Write header
            writer.write("Test Name;Duration Hours;Class Code\n");

            // Write all rows from table
            for(int i = 0; i < model.getRowCount(); i++) {
                String testName = model.getValueAt(i, 0).toString();
                String duration = model.getValueAt(i, 1).toString();
                String classCode = model.getValueAt(i, 2).toString();

                writer.write(testName + ";" + duration + ";" + classCode + "\n");
            }

            writer.close();
            System.out.println("classtest.txt updated successfully!");

        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Error updating file: " + e.getMessage(), "File Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }

